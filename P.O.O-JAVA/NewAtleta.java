package br.org.serratec;

public class NewAtleta {

	private String nome, posicao;
	private int idade;
	public NewAtleta(String nome, String posicao, int idade) {
		super();
		this.nome = nome;
		this.posicao = posicao;
		this.idade = idade;
	}
	public String getNome() {
		return nome;
	}
	public String getPosicao() {
		return posicao;
	}
	public int getIdade() {
		return idade;
	}
	
	
	
}
